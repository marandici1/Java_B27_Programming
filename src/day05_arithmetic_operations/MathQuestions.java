package day05_arithmetic_operations;

public class MathQuestions {
    public static void main(String[] args) {
        System.out.println(4+5);
        System.out.println(4*5);
        // int*int
        System.out.println(2*5);

        //int * double---> double
        System.out.println(2* 5.5);

        // int /int -> int
        System.out.println(10/3);

        // int / double--->double
        System.out.println(10/3.0);

        System.out.println(5% 2); // 2+2 --> 2 went in to 5 twice, there is one left over
        System.out.println(4%2); // no left over 2 divedes into 4 is equal 0
        System.out.println(4%10); // 4 /10, not enough to have a whole 10, 4 is the left over



    }
}
